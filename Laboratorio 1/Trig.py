# Importar modulo Math,operaciones Trigonometricas#

import math


class Trig:
    def __init__(self):
        self.pi = math.pi

    def get_sine_of_pi(self):
        return math.sin(self.pi)

    def get_cosine_of_pi(self):
        return math.cos(self.pi)

    def get_tangent_of_pi(self):
        return math.tan(self.pi)
